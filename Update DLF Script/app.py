def preserve_precision_update(df, replacement_data, df_name):
    """Update dataframe while preserving full numeric precision as strings"""
    if 'Mnemo' not in df.columns:
        print(f"Warning: 'Mnemo' column not found in {df_name}")
        return df
    
    updated_df = df.copy()
    original_count = len(updated_df)
    
    # Target columns for precision preservation
    target_columns = ['t0 IV', 't0 IV unround', 'Mkt Cap']
    existing_columns = [col for col in target_columns if col in updated_df.columns]
    
    if not existing_columns:
        print(f"Warning: None of the target columns found in {df_name}")
        return updated_df
    
    # Clean Mnemo columns
    updated_df['Mnemo'] = updated_df['Mnemo'].str.strip()
    replacement_data['Mnemo'] = replacement_data['Mnemo'].str.strip()
    
    # Create lookup dictionary for each column to preserve precision
    updates_made = 0
    for col in existing_columns:
        if col in replacement_data.columns:
            # Create lookup dictionary - keep as strings
            lookup_dict = dict(zip(replacement_data['Mnemo'], replacement_data[col].astype(str)))
            
            # Update values using lookup
            mask = updated_df['Mnemo'].isin(lookup_dict.keys())
            before_count = mask.sum()
            
            if before_count > 0:
                # Apply updates as strings to preserve precision
                updated_df.loc[mask, col] = updated_df.loc[mask, 'Mnemo'].map(lookup_dict)
                updates_made += before_count
                print(f"  Updated {before_count} values in column '{col}'")
    
    print(f"\n{df_name} Update Summary:")
    print(f"  Total records: {original_count}")
    print(f"  Total updates made: {updates_made}")
    print(f"  Columns updated: {existing_columns}")
    
    return updated_df

def preserve_precision_stock_update(df, stock_replacement_data, df_name):
    """Update stock dataframe while preserving full numeric precision"""
    required_columns = ['Index', '#Symbol']
    missing_columns = [col for col in required_columns if col not in df.columns]
    
    if missing_columns:
        print(f"Warning: Missing columns in {df_name}: {missing_columns}")
        return df
    
    updated_df = df.copy()
    original_count = len(updated_df)
    
    # Target columns for precision preservation
    target_columns = ['Index Mkt Cap', 'Close Prc', 'Pct Wght', 'Adj Closing price']
    existing_columns = [col for col in target_columns if col in updated_df.columns]
    
    if not existing_columns:
        print(f"Warning: None of the target columns found in {df_name}")
        return updated_df
    
    # Clean lookup columns
    updated_df['Index'] = updated_df['Index'].str.strip()
    updated_df['#Symbol'] = updated_df['#Symbol'].str.strip()
    stock_replacement_data['Index'] = stock_replacement_data['Index'].str.strip()
    stock_replacement_data['#Symbol'] = stock_replacement_data['#Symbol'].str.strip()
    
    # Create composite lookup keys
    updated_df['_lookup_key'] = updated_df['Index'] + '|' + updated_df['#Symbol']
    stock_replacement_data['_lookup_key'] = stock_replacement_data['Index'] + '|' + stock_replacement_data['#Symbol']
    
    # Create lookup dictionaries for each column
    updates_made = 0
    for col in existing_columns:
        if col in stock_replacement_data.columns:
            # Create lookup dictionary - keep as strings
            lookup_dict = dict(zip(stock_replacement_data['_lookup_key'], 
                                 stock_replacement_data[col].astype(str)))
            
            # Update values using lookup
            mask = updated_df['_lookup_key'].isin(lookup_dict.keys())
            before_count = mask.sum()
            
            if before_count > 0:
                # Apply updates as strings to preserve precision
                updated_df.loc[mask, col] = updated_df.loc[mask, '_lookup_key'].map(lookup_dict)
                updates_made += before_count
                print(f"  Updated {before_count} values in column '{col}'")
    
    # Drop temporary lookup key
    updated_df = updated_df.drop(columns=['_lookup_key'])
    
    print(f"\n{df_name} Update Summary:")
    print(f"  Total records: {original_count}")
    print(f"  Total updates made: {updates_made}")
    print(f"  Columns updated: {existing_columns}")
    
    return updated_df

def save_precision_files(df, filepath, numeric_columns):
    """Save files with multiple precision-preserving formats"""
    base_path = filepath.rsplit('.', 1)[0]
    
    # Clean the dataframe before saving - replace 'nan' strings and NaN values
    df_clean = df.copy()
    
    # Replace 'nan' strings and NaN values with empty strings for better readability
    # But preserve actual numeric values
    for col in df_clean.columns:
        # Convert to string first, then clean up
        df_clean[col] = df_clean[col].astype(str)
        # Replace 'nan', 'NaN', '<NA>' with empty string
        df_clean[col] = df_clean[col].replace(['nan', 'NaN', '<NA>', 'None'], '')
        # Also handle actual NaN values that might still exist
        df_clean[col] = df_clean[col].fillna('')
    
    # 1. Save as pure text CSV (preserves all precision)
    text_csv_path = f"{base_path}_TEXT.csv"
    df_clean.to_csv(text_csv_path, sep=';', index=False, encoding='utf-8-sig', 
                    quoting=1, na_rep='')  # Replace NaN with empty string
    
    # 2. Save with apostrophe prefix for Excel text interpretation
    df_excel_text = df_clean.copy()
    for col in numeric_columns:
        if col in df_excel_text.columns:
            # Only add apostrophe if the cell is not empty
            df_excel_text[col] = df_excel_text[col].apply(
                lambda x: "'" + str(x) if str(x).strip() != '' else ''
            )
    
    excel_text_path = f"{base_path}_EXCEL_TEXT.csv"
    df_excel_text.to_csv(excel_text_path, sep=';', index=False, encoding='utf-8-sig', na_rep='')
    
    # 3. Save as Excel with column formatting
    excel_path = f"{base_path}.xlsx"
    try:
        with pd.ExcelWriter(excel_path, engine='openpyxl') as writer:
            # Use cleaned dataframe for Excel too
            df_clean.to_excel(writer, sheet_name='Data', index=False, na_rep='')
            
            # Format numeric columns as text to preserve precision
            workbook = writer.book
            worksheet = writer.sheets['Data']
            
            for col in numeric_columns:
                if col in df_clean.columns:
                    col_idx = list(df_clean.columns).index(col) + 1
                    if col_idx <= 26:  # Handle first 26 columns
                        col_letter = chr(64 + col_idx)
                        # Set column format to text
                        for row in range(2, len(df_clean) + 2):  # Skip header
                            cell = worksheet[f"{col_letter}{row}"]
                            cell.number_format = '@'  # Text format
    except Exception as e:
        print(f"Warning: Could not create Excel file {excel_path}: {e}")
        excel_path = None
    
    print(f"Saved precision-preserving files:")
    print(f"  - {text_csv_path} (Pure text CSV)")
    print(f"  - {excel_text_path} (Excel-friendly with apostrophes)")
    if excel_path:
        print(f"  - {excel_path} (Excel with text formatting)")
    
    return text_csv_path, excel_text_path, excel_path

